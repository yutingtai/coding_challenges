def diagonalDifference(arr):
    primary = 0
    secondary = 0
    for i in range(len(arr)):
        primary += arr[i][i]
        secondary += arr[i][len(arr) - 1 - i]
    print(abs(primary - secondary))


if __name__ == '__main__':

    n = int(input().strip())

    arr = []

    for _ in range(n):
        arr.append(list(map(int, input().rstrip().split())))

    result = diagonalDifference(arr)
